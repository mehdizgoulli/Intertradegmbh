const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12 }
);
revealTargets.forEach((el) => revealObserver.observe(el));

// ===== Animated stat counters =====
const counters = document.querySelectorAll(".stats strong[data-count]");

const animateCount = (el) => {
  const target = parseInt(el.dataset.count, 10);
  const suffix = el.dataset.suffix || "";
  const duration = 1500;
  const start = performance.now();

  const tick = (now) => {
    const progress = Math.min((now - start) / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    el.textContent = Math.round(target * eased).toLocaleString() + suffix;
    if (progress < 1) requestAnimationFrame(tick);
  };
  requestAnimationFrame(tick);
};

const countObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        animateCount(entry.target);
        countObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.5 }
);
counters.forEach((el) => countObserver.observe(el));

// ===== Contact form (client-side validation + feedback) =====
const form = document.getElementById("contactForm");
const formNote = document.getElementById("formNote");

function showFormFeedback(message, isError = false) {
  formNote.hidden = false;
  formNote.style.color = isError ? "#d9534f" : "";
  formNote.textContent = message;
}

if (form && formNote) {
  form.addEventListener("submit", async (e) => {
    e.preventDefault();
    const required = ["name", "email", "message"];
    let valid = true;

    required.forEach((id) => {
      const field = document.getElementById(id);
      const empty = !field.value.trim();
      const badEmail =
        id === "email" && field.value && !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(field.value);
      if (empty || badEmail) {
        field.classList.add("invalid");
        valid = false;
      } else {
        field.classList.remove("invalid");
      }
    });

    if (!valid) {
      showFormFeedback(translations[currentLang]["form.validation"], true);
      return;
    }

    const submitBtn = form.querySelector('button[type="submit"]');
    const originalLabel = submitBtn ? submitBtn.textContent : "";
    if (submitBtn) {
      submitBtn.disabled = true;
      submitBtn.textContent = translations[currentLang]["form.sending"];
    }

    showFormFeedback(translations[currentLang]["form.sending"]);

    try {
      const response = await fetch("https://api.web3forms.com/submit", {
        method: "POST",
        headers: { Accept: "application/json" },
        body: new FormData(form),
      });
      const data = await response.json();

      if (response.ok && data.success) {
        showFormFeedback(translations[currentLang]["form.success"]);
        form.reset();
      } else {
        showFormFeedback(data.message || translations[currentLang]["form.error"], true);
      }
    } catch (err) {
      showFormFeedback(translations[currentLang]["form.networkError"], true);
    } finally {
      if (submitBtn) {
        submitBtn.disabled = false;
        submitBtn.textContent = translations[currentLang]["form.submit"];
      }
    }
  });
}

